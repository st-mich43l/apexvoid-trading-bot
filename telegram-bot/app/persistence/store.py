# Shared connection pool, created on first use so importing this module never
# opens a socket (keeps unit tests and tooling side-effect free).
_pool: asyncpg.Pool | None = None


async def _get_pool() -> asyncpg.Pool:
  global _pool
  if _pool is None:
    _pool = await asyncpg.create_pool(
      settings.database_url,
      min_size=1,
      max_size=10,
      command_timeout=30,
    )
  return _pool


async def close_pool() -> None:
  """Close the shared pool. Used on shutdown and between tests."""
  global _pool
  if _pool is not None:
    await _pool.close()
    _pool = None


@asynccontextmanager
async def _connect():
  pool = await _get_pool()
  async with pool.acquire() as conn:
    yield conn


def _rowcount(status: str) -> int:
  """Parse asyncpg's command tag (e.g. ``'UPDATE 3'``) into a row count."""
  try:
    return int(status.split()[-1])
  except (ValueError, IndexError, AttributeError):
    return 0


def _current_trade_date() -> str:
  tz = ZoneInfo(settings.seq_reset_tz)
  return datetime.now(tz).date().isoformat()


