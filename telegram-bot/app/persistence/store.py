async def init_db() -> None:
  """Create all tables and indexes if they do not already exist.

  Safe to call on every application startup — every statement uses
  ``IF NOT EXISTS`` so it is a no-op when the schema is already in place.
  Call this once on startup before any other DB function.
  """
  async with _connect() as db:
    # ------------------------------------------------------------------
    # Table: pips_log
    # Appended to whenever the bot auto-edits a channel message that
    # contains a pips result (e.g. "+80 pips").  Used by /trade_pips for
    # local period summaries.
    # ------------------------------------------------------------------
    await db.execute(
      """
      CREATE TABLE IF NOT EXISTS pips_log (
        id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        ts         BIGINT  NOT NULL,      -- unix timestamp of the edit
        sign       TEXT    NOT NULL,      -- '+' for profit, '-' for loss
        pips       INTEGER NOT NULL,      -- absolute pip count (always positive)
        message_id BIGINT,                -- Telegram message_id that was edited
        chat_id    TEXT,                  -- Telegram chat_id of the channel
        signal_id  BIGINT                 -- linked manual_signals row, if any
      )
      """
    )
    await db.execute("CREATE INDEX IF NOT EXISTS idx_pips_ts ON pips_log(ts)")
    await db.execute(
      "CREATE INDEX IF NOT EXISTS idx_pips_signal_id ON pips_log(signal_id)"
    )

    # ------------------------------------------------------------------
    # Table: manual_signals
    # Tracks every signal posted to the channel via the bot's DM
    # interface ("gold sell 4100-4105 / sl 4110 / tp 95/90/80").
    # ------------------------------------------------------------------
    await db.execute(
      """
      CREATE TABLE IF NOT EXISTS manual_signals (
        id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        ts                 BIGINT           NOT NULL,
        action             TEXT             NOT NULL,
        entry              DOUBLE PRECISION NOT NULL,
        entry_end          DOUBLE PRECISION,
        sl                 DOUBLE PRECISION NOT NULL,
        original_sl        DOUBLE PRECISION,
        tps                TEXT             NOT NULL,
        order_type         TEXT             NOT NULL,
        channel_message_id BIGINT,
        status             TEXT             NOT NULL DEFAULT 'open',
        result_pips        INTEGER,
        closed_at          BIGINT,
        daily_seq          INTEGER,
        trade_date         TEXT,
        fill_state         TEXT             NOT NULL DEFAULT 'pending',
        filled_at          BIGINT,
        legs               TEXT             NOT NULL DEFAULT '[]',
        parent_id          BIGINT,
        setup_type         TEXT,
        confluence         INTEGER,
        note               TEXT,
        symbol             TEXT             NOT NULL DEFAULT 'XAU',
        visibility         TEXT             NOT NULL DEFAULT 'both',
        algo_armed         BOOLEAN          NOT NULL DEFAULT FALSE,
        trade_stream       TEXT             NOT NULL DEFAULT 'manual'
      )
      """
    )
    # Migration: original_sl records the stop as first placed and is never
    # updated (update_sl only moves `sl`). Older rows predate the column, so
    # add it idempotently and backfill from the current stop.
    await db.execute(
      "ALTER TABLE manual_signals "
      "ADD COLUMN IF NOT EXISTS original_sl DOUBLE PRECISION"
    )
    await db.execute(
      "UPDATE manual_signals SET original_sl = sl WHERE original_sl IS NULL"
    )
    # Migration: execution lifecycle columns for the opt-in `/ algo` DM
    # suffix (manual-signal broker execution, PR 2 of 3 — infra only, no
    # broker consumes these yet). Every pre-existing row predates broker
    # execution entirely, so NULL/default is correct for all of them; there
    # is nothing to backfill, unlike original_sl above.
    await db.execute(
      # 'notify' (default, today's behavior) | 'algo' (owner armed broker
      # execution for this signal via the `/ algo` suffix).
      "ALTER TABLE manual_signals "
      "ADD COLUMN IF NOT EXISTS execution_mode TEXT NOT NULL DEFAULT 'notify'"
    )
    await db.execute(
      # NULL (never armed) | 'armed' | 'filled' | 'closed' | 'cancelled' |
      # 'rejected' | 'error'.
      "ALTER TABLE manual_signals "
      "ADD COLUMN IF NOT EXISTS execution_status TEXT"
    )
    await db.execute(
      # Stable id of the published ManualTradeIntent, 'manual:<id>:<revision>'.
      "ALTER TABLE manual_signals "
      "ADD COLUMN IF NOT EXISTS execution_intent_id TEXT"
    )
    await db.execute(
      # Bumped each time a new intent is published for the same signal
      # (e.g. a re-arm after SL/TP edit) — not used anywhere yet in this PR.
      "ALTER TABLE manual_signals "
      "ADD COLUMN IF NOT EXISTS execution_revision INTEGER NOT NULL DEFAULT 0"
    )
    await db.execute(
      # Broker-side position id once filled. Internal only — never rendered
      # to Telegram.
      "ALTER TABLE manual_signals "
      "ADD COLUMN IF NOT EXISTS broker_position_id TEXT"
    )
    await db.execute(
      # Actual broker fill price, once filled.
      "ALTER TABLE manual_signals "
      "ADD COLUMN IF NOT EXISTS broker_fill_price DOUBLE PRECISION"
    )
    await db.execute(
      # Last error message from an arm/fill/close attempt, if any.
      "ALTER TABLE manual_signals "
      "ADD COLUMN IF NOT EXISTS execution_error TEXT"
    )
    await db.execute(
      "ALTER TABLE manual_signals "
      "ADD COLUMN IF NOT EXISTS algo_armed BOOLEAN NOT NULL DEFAULT FALSE"
    )
    await db.execute(
      "ALTER TABLE manual_signals "
      "ADD COLUMN IF NOT EXISTS trade_stream TEXT NOT NULL DEFAULT 'manual'"
    )
    await db.execute(
      "CREATE INDEX IF NOT EXISTS idx_manual_signals_execution_intent_id "
      "ON manual_signals(execution_intent_id)"
    )
    await db.execute(
      "CREATE INDEX IF NOT EXISTS idx_manual_signals_status "
      "ON manual_signals(status)"
    )
    await db.execute(
      "CREATE INDEX IF NOT EXISTS idx_manual_signals_trade_date "
      "ON manual_signals(trade_date)"
    )
    await db.execute(
      "CREATE INDEX IF NOT EXISTS idx_manual_signals_symbol_trade_date "
      "ON manual_signals(symbol, trade_date)"
    )
    await db.execute(
      "CREATE INDEX IF NOT EXISTS idx_manual_signals_parent_id "
      "ON manual_signals(parent_id)"
    )

    # Broker execution ledger. Fills and results are separate so scale-ins
    # retain their real fill count while one group result remains one trade.
    await db.execute(
      """
      CREATE TABLE IF NOT EXISTS auto_trade_fills (
        position_id BIGINT PRIMARY KEY,
        group_id    TEXT             NOT NULL,
        trade_key   TEXT             NOT NULL,
        trade_stream TEXT            NOT NULL,
        symbol      TEXT             NOT NULL DEFAULT 'XAU',
        setup_type  TEXT,
        direction   TEXT,
        entry_price DOUBLE PRECISION,
        stop_pips   DOUBLE PRECISION,
        volume      BIGINT,
        filled_at   BIGINT           NOT NULL
      )
      """
    )
    await db.execute(
      "CREATE INDEX IF NOT EXISTS idx_auto_trade_fills_group "
      "ON auto_trade_fills(group_id)"
    )
    await db.execute(
      """
      CREATE TABLE IF NOT EXISTS auto_trade_results (
        group_id     TEXT PRIMARY KEY,
        trade_key    TEXT             NOT NULL,
        trade_stream TEXT             NOT NULL,
        result_pips  DOUBLE PRECISION NOT NULL,
        closed_at    BIGINT           NOT NULL
      )
      """
    )
    await db.execute(
      "CREATE INDEX IF NOT EXISTS idx_auto_trade_results_closed "
      "ON auto_trade_results(closed_at)"
    )

    await db.execute(
      """
      CREATE TABLE IF NOT EXISTS signal_posts (
        signal_id  BIGINT NOT NULL,
        channel_id BIGINT NOT NULL,
        message_id BIGINT NOT NULL,
        tier       TEXT   NOT NULL,
        PRIMARY KEY (signal_id, channel_id)
      )
      """
    )
    await db.execute(
      "CREATE UNIQUE INDEX IF NOT EXISTS idx_signal_posts_message "
      "ON signal_posts(channel_id, message_id)"
    )
    # Back-fill VIP posts for signals stored before signal_posts existed.
    await db.execute(
      """
      INSERT INTO signal_posts (signal_id, channel_id, message_id, tier)
      SELECT id, $1, channel_message_id, 'vip'
      FROM manual_signals
      WHERE channel_message_id IS NOT NULL
      -- Unqualified DO NOTHING mirrors SQLite's INSERT OR IGNORE: skip a row
      -- that collides on *either* unique constraint (PK or message_id).
      ON CONFLICT DO NOTHING
      """,
      settings.signal_vip_channel_id,
    )

    await db.execute(
      """
      CREATE TABLE IF NOT EXISTS events (
        event_id  TEXT    PRIMARY KEY,
        ts_utc    BIGINT  NOT NULL,
        currency  TEXT    NOT NULL,
        title     TEXT    NOT NULL,
        impact    TEXT    NOT NULL,
        forecast  TEXT,
        previous  TEXT,
        actual    TEXT,
        all_day   INTEGER NOT NULL DEFAULT 0,
        source    TEXT    NOT NULL DEFAULT 'ff',
        synced_at BIGINT  NOT NULL
      )
      """
    )
    await db.execute("CREATE INDEX IF NOT EXISTS idx_events_ts ON events(ts_utc)")
    await db.execute(
      """
      CREATE TABLE IF NOT EXISTS meta (
        key   TEXT PRIMARY KEY,
        value TEXT
      )
      """
    )


