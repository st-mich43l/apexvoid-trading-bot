async def mark_filled(row_id: int) -> dict | None:
  """Mark a pending open signal filled and return its pre-update row."""
  await expire_unfilled_pending_signals()
  async with _connect() as db:
    async with db.transaction():
      row = await db.fetchrow(
        "SELECT * FROM manual_signals "
        "WHERE id = $1 AND status = 'open' AND fill_state = 'pending' "
        "FOR UPDATE",
        row_id,
      )
      if row is None:
        return None
      await db.execute(
        "UPDATE manual_signals SET fill_state = 'filled', filled_at = $1 "
        "WHERE id = $2 AND status = 'open' AND fill_state = 'pending'",
        int(time.time()), row_id,
      )
    return dict(row)


async def close_leg(
  row_id: int,
  pips: int,
  frac: float | None = None,
) -> dict | None:
  """Book one scale-out leg and close the signal when no size remains."""
  async with _connect() as db:
    async with db.transaction():
      row = await db.fetchrow(
        "SELECT * FROM manual_signals WHERE id = $1 AND status = 'open' "
        "FOR UPDATE",
        row_id,
      )
      if row is None:
        return None

      legs = json.loads(row["legs"] or "[]")
      used = sum(float(leg["frac"]) for leg in legs)
      remaining = max(0.0, 1.0 - used)
      close_frac = remaining if frac is None else frac
      result_base = {
        "id": row["id"],
        "channel_message_id": row["channel_message_id"],
        "daily_seq": row["daily_seq"],
        "symbol": row["symbol"],
        "visibility": row["visibility"],
      }
      if close_frac <= 0 or close_frac > remaining + _LEG_EPSILON:
        return {
          **result_base,
          "error": "exceeds_remaining",
          "remaining": remaining,
        }

      now = int(time.time())
      legs.append({"frac": close_frac, "pips": pips, "ts": now})
      new_remaining = 1.0 - sum(float(leg["frac"]) for leg in legs)
      if new_remaining <= _LEG_EPSILON:
        net = round(
          sum(float(leg["frac"]) * int(leg["pips"]) for leg in legs)
        )
        await db.execute(
          "UPDATE manual_signals SET status = 'closed', result_pips = $1, "
          "closed_at = $2, legs = $3 WHERE id = $4 AND status = 'open'",
          net, now, json.dumps(legs), row_id,
        )
        return {
          **result_base,
          "closed": True,
          "net": net,
          "remaining": 0.0,
          "frac": close_frac,
        }

      await db.execute(
        "UPDATE manual_signals SET legs = $1 "
        "WHERE id = $2 AND status = 'open'",
        json.dumps(legs), row_id,
      )
    return {
      **result_base,
      "closed": False,
      "net": None,
      "remaining": new_remaining,
      "frac": close_frac,
    }


